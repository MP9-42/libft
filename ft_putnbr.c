#include "libft.h"

void	ft_putnbr(int nb)
{
	char	c;
	int		psy;

	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		write(1, "-", 1);
		nb = nb * (-1);
	}
	if (nb / 10 > 0)
		ft_putnbr(nb / 10);
	psy = nb % 10;
	c = psy + '0';
	write (1, &c, 1);
}
